package com.app.tigerpay.Model;

import java.io.Serializable;

public class ActivitiesModel implements Serializable {

    String id,name,image,type,btc_rate,btc,date_time;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getBtc_rate() {
        return btc_rate;
    }

    public void setBtc_rate(String btc_rate) {
        this.btc_rate = btc_rate;
    }

    public String getBtc() {
        return btc;
    }

    public void setBtc(String btc) {
        this.btc = btc;
    }

    public String getDate_time() {
        return date_time;
    }

    public void setDate_time(String date_time) {
        this.date_time = date_time;
    }
}
